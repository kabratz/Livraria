    private void jtbEditoraMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtbEditoraMousePressed
        // TODO add your handling code here:
        try{
            
            int linhaSelecionada = jtbEditora.getSelectedRow();//pega a linha selecionada
            String codigo = jtbEditora.getModel().getValueAt(linhaSelecionada, 0).toString(); // Primeira coluna da linha
            String nome = jtbEditora.getModel().getValueAt(linhaSelecionada, 2).toString();
            String cnpj = jtbEditora.getModel().getValueAt(linhaSelecionada, 1).toString();
//Verifica se clicou na coluna 2 = EXCLUIR
            if(jtbEditora.getSelectedColumn() == 3){
                try{
                    
                    boolean wPergunta = CaixaDeDialogo.obterinstancia().pedirConfirmacao("Tem certeza de que deseja excluir?","",'p');
                    if (wPergunta == true){
                        lblId.setText(codigo);
                        objEditora = new Editora();
                        objEditora.setId_editora(Integer.parseInt(lblId.getText()));
                        
                        objEditoraControle = new EditoraController(objEditora, null);
                        boolean wControle = objEditoraControle.excluir();
                        if (wControle){
                            CaixaDeDialogo.obterinstancia().exibirMensagem("Exclu√≠do com Sucesso!");
                        }else{
                            CaixaDeDialogo.obterinstancia().exibirMensagem("Erro ao excluir!");
                        }
                    }

                }catch(Exception ex){
                    CaixaDeDialogo.obterinstancia().exibirMensagem("Erro: " + ex.getMessage());
                }
            }else {
                lblId.setText(codigo);   
                txtNome.setText(nome);
                txtCnpj.setText(cnpj);
            }
        
        }catch(Exception ex){
            CaixaDeDialogo.obterinstancia().exibirMensagem(ex.getMessage(), 'e');
        }
        atualizarTabela();
    }//GEN-LAST:event_jtbEditoraMousePressed

