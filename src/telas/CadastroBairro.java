    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        try{
            boolean retorno;
            //validar os campos
            if(txtNome.getText().trim().length() == 0 || cbCidade.getSelectedIndex() == 0){
                CaixaDeDialogo.obterinstancia().exibirMensagem("Informe uma livraria ou cidade corretamente", 'a');
                return;
            }

            objBairro = new Bairro();
            Combos c = (Combos) cbCidade.getSelectedItem();
            String codCidade = c.getCodigo();
            objBairro.setId_cidade(Integer.parseInt(codCidade));
            objBairro.setNome(txtNome.getText());
            if(!lblId.getText().equals("ID")){
                objBairro.setId_bairro(Integer.parseInt(lblId.getText()));
                objBairroControle = new BairroController(objBairro, null);
                retorno = objBairroControle.alterar();
            }else{
                objBairroControle = new BairroController(objBairro, null);
   
                retorno = objBairroControle.incluir();
            }
            
            if(retorno = true){
                CaixaDeDialogo.obterinstancia().exibirMensagem("Registro salvo");
            }else{
                CaixaDeDialogo.obterinstancia().exibirMensagem("Erro ao tentar salvar");
            }
            
            atualizarTabela();

        }catch(Exception ex){
            CaixaDeDialogo.obterinstancia().exibirMensagem("Erro ao tentar incluir");
            System.out.println("ERRO: " + ex.getMessage().toString());
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

    private void jtbBairroMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtbBairroMousePressed
        try{
            
            int linhaSelecionada = jtbBairro.getSelectedRow();//pega a linha selecionada
            String codigo = jtbBairro.getModel().getValueAt(linhaSelecionada, 0).toString(); // Primeira coluna da linha
            //Verifica se clicou na coluna 2 = EXCLUIR
            if(jtbBairro.getSelectedColumn() == 3){
                try{
                    
                    boolean wPergunta = CaixaDeDialogo.obterinstancia().pedirConfirmacao("Tem certeza de que deseja excluir?","",'p');
                    if (wPergunta == true){
                        lblId.setText(codigo);
                        objBairro = new Bairro();
                        objBairro.setId_bairro(Integer.parseInt(lblId.getText()));
                        
                        objBairroControle = new BairroController(objBairro, null);
                        boolean wControle = objBairroControle.excluir();
                        if (wControle){
                            CaixaDeDialogo.obterinstancia().exibirMensagem("Exclu√≠do com Sucesso!");
                        }else{
                            CaixaDeDialogo.obterinstancia().exibirMensagem("Erro ao excluir!");
                        }
                    }

                }catch(Exception ex){
                    CaixaDeDialogo.obterinstancia().exibirMensagem("Erro: " + ex.getMessage());
                }
            }else {
                objBairroControle = new BairroController(objBairro, null);
                objBairro = objBairroControle.buscar(codigo);
                
                lblId.setText(codigo);   
                cbCidades.SetaComboBox(String.valueOf(objBairro.getId_cidade()));
                txtNome.setText(objBairro.getNome());
            }
        
        }catch(Exception ex){
            CaixaDeDialogo.obterinstancia().exibirMensagem(ex.getMessage(), 'e');
        }
        atualizarTabela();
    }//GEN-LAST:event_jtbBairroMousePressed

            private void preencherCombos() {
        try {
            cbCidades = new Combos(cbCidade);        
            cbCidades.PreencheCombo("SELECT id_cidade, nome FROM cidade");
        } catch (Exception e) {
            CaixaDeDialogo.obterinstancia().exibirMensagem(e.getMessage());
        }
    }
         private void limparTela(){
        try{
            lblId.setText("ID");
            txtNome.setText("");
            cbCidade.setSelectedIndex(0);
           
            btnSalvar.setEnabled(true);
            
            atualizarTabela();
            
        }catch(Exception ex){
            CaixaDeDialogo.obterinstancia().exibirMensagem("Erro: " + ex.getMessage());
        }
    }
