    private void cbCidadeItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cbCidadeItemStateChanged
                 
        try {
            if (cbCidade.getSelectedIndex() > 0) {
                
            Combos c = (Combos) cbCidade.getSelectedItem();
            String codCidade = c.getCodigo();
        
            
            cbCombosBairro.PreencheCombo("SELECT b.id_bairro, b.nome FROM bairro b, cidade c WHERE b.id_cidade = c.id_cidade AND c.id_cidade =" + codCidade);
            cbCombosBairro.SetaComboBox(codCidade);
            }
        } catch (SQLException ex) {
            Logger.getLogger(CadastroLivraria.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_cbCidadeItemStateChanged
